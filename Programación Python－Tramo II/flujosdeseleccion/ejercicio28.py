
"""
Crear un programa que pida un número de mes (ejemplo 4) y escriba el nombre del 
mes en letras ("abril"). Verificar que el mes sea válido e informar en caso que no lo sea.
"""

num_mes = int(input("Ingrese en numero uno de los 12 meses del año: "))
print("\n")
if num_mes == 1:
    print("Enero")
elif num_mes == 2:
    print("Febrero")
elif num_mes == 3:
    print("Marzo")
elif num_mes == 4:
    print("Abril")
elif num_mes == 5:
    print("Mayo")
elif num_mes == 6:
    print("Junio")
elif num_mes == 7:
    print("Julio")
elif num_mes == 8:
    print("Agosto")
elif num_mes == 9:
    print("Septiembre")
elif num_mes == 10:
    print("Octubre")
elif num_mes == 11:
    print("Noviembre")
else:
    print("Diciembre")



